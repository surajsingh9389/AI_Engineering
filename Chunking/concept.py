# Defination: Chunking is the process of breaking down large amounts of information into smaller, more manageable units or "chunks". Depending on whether you are looking at it from a psychological (human memory) or technological (AI and data processing) perspective, the types and examples vary significantly. 

# 🧩 Types of Chunking (Concept Only)
# 1. Fixed-size chunking

# Split every N tokens

# ✔ Simple
# ❌ Breaks meaning

# 2. Sentence-based chunking

# Split by sentences

# ✔ Better meaning
# ❌ Still can lose context across sentences

# 3. Semantic chunking (IMPORTANT)

# Split based on meaning

# ✔ Best quality
# ❌ More complex

# 4. Overlapping chunks (VERY IMPORTANT)

# Instead of:

# Chunk 1: A B C
# Chunk 2: D E F

# We do:

# Chunk 1: A B C
# Chunk 2: C D E

# 👉 Overlap preserves context